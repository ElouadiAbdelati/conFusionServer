const mongoose = require('mongoose');
const Shema = mongoose.Schema();

const userShema = new Shema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    admin: {
        type: Boolean,
        default: false
    }
});

module.exports = mongoose.model('User', userShema);